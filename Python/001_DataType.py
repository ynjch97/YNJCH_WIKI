# Number
# type() : 변수나 데이터의 자료형 리턴
a = 10
b = 20
c = a + b
print(a + b + c)
print(a, b, c)
print(type(a))

# Float
a = 1.6
b = 2.4
c = a + b
print(a)
print(c)
print(type(c))

# String
a = '10'
b = '20'
c = a + b
print(a + b + c)
print(a, b, c)
print(type(a))

# Boolean
a = True
b = False
c = 20 < 40
print(a, b, c)
print(a + b)
print(a * b)
print(type(a))
print(type(a + b))
